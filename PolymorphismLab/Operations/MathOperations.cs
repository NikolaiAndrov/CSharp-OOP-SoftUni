namespace Operations
{
    public class MathOperations
    {
        public int Add(int a, int b)
        {
            return a + b;
        }

        public double Add(double a, double b, double n)
        {
            return a + b + n;
        }

        public decimal Add(decimal a, decimal b, decimal n)
        {
            return a + b + n;
        }
    }
}
